  vcode: /^[a-zA-Z0-9]{0,}$/,
  // 数字
  number: /^[0-9]+$/,
  // 中文字符
  chinese: /[\u4e00-\u9fa5]/,
  //数字和字母 (一般用于图片验证码)
  alphaNumber: /^[a-zA-Z0-9]{0,}$/,
